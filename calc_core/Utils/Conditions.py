"""Термобарические условия — давление и температура для одного расчёта."""


class Conditions:
    '''
    Класс для хранения термобарических условий.

    Parameters
    ----------
    p : float
        Давление, бар.
    t : float
        Температура на входе — **в °C**, конвертируется в Кельвины внутри.

    Известный баг (см. CLAUDE.md Known Issues): `self.t = t + 273.14`,
    должно быть `273.15` — небольшое систематическое смещение температуры
    во всех расчётах через `Conditions`. Не исправлено намеренно, не чинить
    попутно без отдельного согласования.
    '''
    def __init__(self, p, t):
        self.p = p
        self.t = t + 273.14

class StandardConditions:
    '''
    Class for standard conditions.

    Стандартные условия: `p = 1.01325` бар, `t = 293.14` K (тоже не 293.15 —
    согласовано с тем же смещением, что в `Conditions`, но здесь захардкожено
    напрямую в K, без сложения).
    '''
    def __init__(self):
        self.p = 1.01325
        self.t = 293.14